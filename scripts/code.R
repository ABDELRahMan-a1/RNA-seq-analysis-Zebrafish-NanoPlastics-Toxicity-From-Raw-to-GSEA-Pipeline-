# set path for data files and abundance files
setwd("G:/RNA_seq/PRJNA1175381")

# 1. Load and Prepare Metadata
# --------
# Load sample information from Excel file
samples <- read.xlsx("G:/RNA_seq/PRJNA1175381/PRJNA1175381_metadata.xlsx")
samples <- as.data.frame(samples)

# Set sample names as row names to align with count data
rownames(samples) <- samples$sample

# Convert the experimental variable to a factor
samples$condition <- factor(samples$condition)


# 2. Define Paths to Kallisto Output Files
# ----------------
# Construct file paths for abundance.h5 files generated by Kallisto
files <- file.path("kallisto_out", samples$sample, "abundance.tsv")
names(files) <- samples$sample

# Verify that all specified files exist before proceeding
if(!all(file.exists(files))) {
  stop("Error: One or more abundance.h5 files were not found at the specified paths.")
}
# 3. Create Transcript-to-Gene Mapping (WITH GENE SYMBOLS)
# ------------------------------------------------------------------------------
# Connect to Ensembl BioMart for Danio rerio (Zebrafish)
mart <- useMart("ensembl", dataset = "drerio_gene_ensembl")

# Retrieve transcript IDs, Ensembl IDs, and External Gene Names (Symbols)
tx2gene_raw <- getBM(attributes = c("ensembl_transcript_id", "ensembl_gene_id", "external_gene_name"), 
                     mart = mart)

# Smart Mapping: Use Symbol if available, otherwise fallback to Ensembl ID
# This prevents empty labels in your plots
tx2gene_raw$final_gene <- ifelse(tx2gene_raw$external_gene_name == "", 
                                 tx2gene_raw$ensembl_gene_id, 
                                 tx2gene_raw$external_gene_name)

# Create the final 2-column mapping required by tximport
tx2gene <- tx2gene_raw[, c("ensembl_transcript_id", "final_gene")]

#  Import Quantification Data with tximport
# ------------------------------------------------------------------------------
# Now 'txi' will use Gene Symbols as row names automatically
txi <- tximport(files, 
                type = "kallisto", 
                tx2gene = tx2gene, 
                ignoreTxVersion = TRUE)

#  Extract and Calculate Statistics
# ------------------------------------------------------------------------------
library(matrixStats) # Ensure matrixStats is loaded for rowSds/rowMedians

tpm_stats <- as.data.frame(txi$abundance) %>%
  setNames(samples$sample) %>%
  mutate(SD  = rowSds(as.matrix(.)), 
         AVG = rowMeans(.), 
         MED = rowMedians(as.matrix(.)))

  # Update row names for tpm_stats to match gene symbols
rownames(tpm_stats) <- rownames(txi$abundance)

#  Export Gene Counts to Excel (Now with readable Gene Names)
library(openxlsx)
write.xlsx(as.data.frame(txi$counts), "Gene_Counts_Matrix_Symbols.xlsx", rowNames = TRUE)

#  Visualization of TPM Distribution 
# ------------------------------------------------------------------------------
ggplot(tpm_stats, aes(x = SD, y = MED)) +
  geom_point(alpha = 0.3, color = "midnightblue") + 
  labs(title = "TPM Distribution: Variability vs. Expression",
       x = "Standard Deviation (SD)", y = "Median Expression (MED)") +
  theme_minimal()

# 5. Construct DESeq2 Object
# ------------------------------------------------------------------------------
# We ensure condition is a factor and 'control' is the reference level
samples$condition <- factor(samples$condition)
#Set 'control' as the reference level for statistical comparison
samples$condition <- relevel(samples$condition, ref = "control")

dds <- DESeqDataSetFromTximport(txi, 
                                colData = samples, 
                                design = ~ condition)

# 6. Pre-processing & Filtering
# ------------------------------------------------------------------------------
# Basic pre-filtering: remove genes with low total counts (at least 10 reads)
# This improves the speed and power of statistical tests
keep <- rowSums(counts(dds)) >= 10
dds <- dds[keep, ]

# Verify the object structure
print(dds)


# 7. Run DESeq2 Pipeline
# ------------------------------------------------------------------------------
dds <- DESeq(dds)


# 8. Data Transformation (For Quality Control Plots)
# ------------------------------------------------------------------------------
# Variance Stabilizing Transformation (VST) minimizes the variance-mean dependence
# blind = FALSE because we want to account for our experimental design
vsd <- vst(dds, blind = FALSE)


# 9. Extract and Shrink Results (LFC Shrinkage)
# ------------------------------------------------------------------------------
# First, extract raw results to set alpha threshold
res_raw <- results(dds, contrast = c("condition", "PS_NPs", "control"), alpha = 0.05)

# Then, apply apeglm shrinkage to Log2 Fold Changes for better accuracy
# res_names[2] ensures we target the PS-NPs vs Control comparison
res_names <- resultsNames(dds)
res_shrunk <- lfcShrink(dds, coef = res_names[2], type = "apeglm")


# 10. Results Processing and Filtering
# ------------------------------------------------------------------------------
# Convert results to a data frame and remove NA values
res_df <- as.data.frame(res_shrunk)
res_clean <- res_df[!is.na(res_df$padj), ]

# Add a significance column based on Padj < 0.05 and |LFC| > 0.5
res_clean$significance <- "Not Significant"
res_clean$significance[res_clean$padj < 0.05 & abs(res_clean$log2FoldChange) > 0.5] <- "Significant"


# 11. Categorize DEGs (Up and Downregulated)
# ------------------------------------------------------------------------------
upregulated <- res_clean[res_clean$log2FoldChange > 0.5 & res_clean$padj < 0.05, ]
downregulated <- res_clean[res_clean$log2FoldChange < -0.5 & res_clean$padj < 0.05, ]


# 12.Visualizations
# ------------------------------------------------------------------------------

# A. PCA Plot
