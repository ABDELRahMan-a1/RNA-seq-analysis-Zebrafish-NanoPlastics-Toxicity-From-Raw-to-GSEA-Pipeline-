  scale_color_brewer(palette = "Set1") +
  ggtitle("Principal Component Analysis (PCA)") +
  geom_text(aes(label = name), vjust = 1.5, size = 3)

# B. MA-Plot
plotMA(res_shrunk, main = "MA-Plot (LFC Shrunk): PS-NPs vs Control", ylim = c(-4, 4))

