# 16. Gene Set Enrichment Analysis (GSEA)
# ------------------------------------------------------------------------------

# 1. Create a ranked list using a combined metric: -log10(pvalue) * sign(log2FoldChange)
res_for_gsea <- res_clean %>%
  mutate(rank_metric = -log10(pvalue) * sign(log2FoldChange)) %>%
  arrange(desc(rank_metric))

# Extract the ranked vector
gene_list <- res_for_gsea$rank_metric
names(gene_list) <- rownames(res_for_gsea)

# Quick check of the top and bottom of your list
head(gene_list) # Strongest Upregulated
tail(gene_list) # Strongest Downregulated


# 2. Convert Gene Symbols to Entrez IDs (Required for KEGG)
ids <- bitr(names(gene_list), 
            fromType = "SYMBOL", 
            toType   = "ENTREZID", 
            OrgDb    = org.Dr.eg.db)

# Merge IDs with our ranked list
# Remove duplicates if any to keep the ranking clean
gene_list_entrez <- gene_list[names(gene_list) %in% ids$SYMBOL]
names(gene_list_entrez) <- ids$ENTREZID[match(names(gene_list_entrez), ids$SYMBOL)]
gene_list_entrez <- sort(gene_list_entrez, decreasing = TRUE)

# 3. Run GSEA for Biological Processes (BP)
gsea_go <- gseGO(geneList     = gene_list, # We can use Symbols directly for GO
                 OrgDb        = org.Dr.eg.db,
                 ont          = "BP", 
                 keyType      = "SYMBOL",
                 minGSSize    = 10,
                 maxGSSize    = 500,
                 pvalueCutoff = 0.05,
                 verbose      = FALSE,
                 seed         = TRUE)

# Export GSEA results to Excel
write.xlsx(as.data.frame(gsea_go), "GSEA_Biological_Processes_Results.xlsx")


