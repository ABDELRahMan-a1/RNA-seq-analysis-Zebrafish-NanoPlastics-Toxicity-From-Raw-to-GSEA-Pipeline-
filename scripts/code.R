# Close the device
dev.off()


# D. Heatmap (Using Z-score Scaling)
# ------------------------------------------------------------------------------
library(pheatmap)

# 1. Select the top 30 genes with the smallest adjusted p-values
top30_genes <- head(rownames(res_clean[order(res_clean$padj), ]), 30)

# 2. Extract VST-transformed counts for these genes
mat <- assay(vsd)[top30_genes, ]

# 3. Apply Z-score Normalization (Scaling by row)
# This centers each gene's expression to its mean and scales by its standard deviation
# Formula: z = (x - mean) / sd
mat_scaled <- t(scale(t(mat)))

# 4. Generate the Heatmap
pheatmap(mat_scaled, 
         annotation_col = as.data.frame(colData(dds)[, "condition", drop = FALSE]),
         show_rownames = TRUE, 
         show_colnames = TRUE,
         cluster_cols = TRUE,    # Cluster samples based on similarity
         cluster_rows = TRUE,    # Cluster genes based on expression patterns
         main = "Top 30 DEGs Heatmap",
         color = colorRampPalette(c("blue", "white", "red"))(100),
         border_color = NA)



